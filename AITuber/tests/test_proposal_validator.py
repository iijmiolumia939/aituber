# ── TC-REFL-12: cmd not in allowlist ──────────────────────────────────────


class TestProposalValidatorCmdAllowlist:
    """TC-REFL-12: cmd がアローリスト外 → INVALID (FR-REFL-03)."""

    def test_unknown_cmd_rejected(self):
        """TC-REFL-12: cmd = 'exec_shell' → INVALID。"""
        validator = ProposalValidator()
        entry = _valid_entry(cmd="exec_shell")
        result = validator.validate(entry)

        assert result.status == ValidationStatus.INVALID
        assert "cmd" in result.reason.lower()

    @pytest.mark.parametrize("cmd", ["avatar_update", "avatar_event"])
    def test_allowed_cmds_accepted(self, cmd: str):
        """TC-REFL-12b: 許可 cmd は受け入れられる。"""
        validator = ProposalValidator()
        if cmd == "avatar_update":
            entry = _valid_entry(cmd=cmd)
        else:
            entry = {"intent": "test_evt", "cmd": cmd, "event": "sparkle"}
        result = validator.validate(entry)

        assert result.status != ValidationStatus.INVALID or result.reason == ""


