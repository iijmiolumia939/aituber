# ── Helpers ────────────────────────────────────────────────────────────────


def _valid_entry(**overrides) -> dict:
    base = {
        "intent": "wave_hello",
        "cmd": "avatar_update",
        "gesture": "wave",
        "priority": 0,
        "notes": "Generated by ReflectionRunner",
    }
    base.update(overrides)
    return base


