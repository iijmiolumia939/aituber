# ── TC-REFL-16〜18: PolicyUpdater ─────────────────────────────────────────


class TestPolicyUpdater:
    """TC-REFL-16〜18: behavior_policy.yml 追記 (FR-REFL-04)."""

    def test_load_policy_returns_list(self, tmp_path):
        """TC-REFL-16: 既存の YAML を読んで dict リストを返す。"""
        yml = tmp_path / "policy.yml"
        yml.write_text(
            "- intent: nod_agreement\n"
            "  cmd: avatar_update\n"
            "  gesture: nod\n",
            encoding="utf-8",
        )
        updater = PolicyUpdater()
        policy = updater.load_policy(str(yml))

        assert len(policy) >= 1
        assert any(e["intent"] == "nod_agreement" for e in policy)

    def test_load_policy_empty_file_returns_empty_list(self, tmp_path):
        """TC-REFL-16b: 空ファイル → 空リストを返す。"""
        yml = tmp_path / "empty.yml"
        yml.write_text("", encoding="utf-8")
        updater = PolicyUpdater()
        policy = updater.load_policy(str(yml))

        assert policy == []

    def test_append_entries_writes_new_entries(self, tmp_path):
        """TC-REFL-17: 新エントリを YAML ファイル末尾に追記し、追記件数を返す。"""
        yml = tmp_path / "policy.yml"
        yml.write_text(
            "- intent: existing_action\n  cmd: avatar_update\n  gesture: wave\n",
            encoding="utf-8",
        )
        updater = PolicyUpdater()
        new_entries = [
            {
                "intent": "new_action_xyz",
                "cmd": "avatar_update",
                "gesture": "nod",
                "notes": "Added by test",
            }
        ]
        count = updater.append_entries(str(yml), new_entries)

        assert count == 1
        # Re-read the file and verify new entry exists
        policy_after = updater.load_policy(str(yml))
        assert any(e["intent"] == "new_action_xyz" for e in policy_after)
        # Original entry still there
        assert any(e["intent"] == "existing_action" for e in policy_after)

    def test_append_entries_empty_list_returns_zero(self, tmp_path):
        """TC-REFL-18: 空リストを渡した場合 → 0 を返し、ファイルは変更されない。"""
        yml = tmp_path / "policy.yml"
        original = "- intent: stay_action\n  cmd: avatar_update\n  gesture: idle\n"
        yml.write_text(original, encoding="utf-8")
        updater = PolicyUpdater()
        count = updater.append_entries(str(yml), [])

        assert count == 0
        assert yml.read_text(encoding="utf-8") == original

    def test_append_entries_creates_file_if_missing(self, tmp_path):
        """TC-REFL-17b: ファイルが存在しない場合、新規作成して追記する。"""
        yml = tmp_path / "new_policy.yml"
        updater = PolicyUpdater()
        new_entries = [{"intent": "bootstrap_action", "cmd": "avatar_update", "gesture": "wave"}]
        count = updater.append_entries(str(yml), new_entries)

        assert count == 1
        assert yml.exists()
        policy = updater.load_policy(str(yml))
        assert any(e["intent"] == "bootstrap_action" for e in policy)
