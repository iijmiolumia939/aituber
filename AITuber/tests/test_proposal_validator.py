# ── TC-REFL-14: duplicate intent ──────────────────────────────────────────


class TestProposalValidatorDuplicate:
    """TC-REFL-14: 既存ポリシーと重複 intent → DUPLICATE (FR-REFL-03)."""

    def test_duplicate_intent_returns_duplicate(self):
        """TC-REFL-14: 既存ポリシーに同名 intent が存在 → DUPLICATE。"""
        existing = [{"intent": "nod_agreement", "cmd": "avatar_update", "gesture": "nod"}]
        validator = ProposalValidator(existing_policy=existing)
        entry = _valid_entry(intent="nod_agreement")
        result = validator.validate(entry)

        assert result.status == ValidationStatus.DUPLICATE

    def test_no_existing_policy_no_duplicate(self):
        """TC-REFL-14b: 既存ポリシーが空なら DUPLICATE にならない。"""
        validator = ProposalValidator(existing_policy=[])
        entry = _valid_entry(intent="brand_new_action")
        result = validator.validate(entry)

        assert result.status == ValidationStatus.VALID


